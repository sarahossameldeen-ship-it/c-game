void movehero(char x[][172], int& r, int& c, char let, int& flag, int& ctjump, int flaggun, int& up, int& down, int& right, int& left, int rl, int cl)
{
   
        if (let == 'd' && c < 171) {  // right
            flag = 1;
            c += (flaggun == 1) ? 6 : 2;
        }
        if (let == 'a' && c > 5) {    // left
            flag = 2;
            c -= (flaggun == 1) ? 6 : 2;
        }

    if (let == 'd' && c < 171)  //right
    {
        flag = 1;
        if (flaggun == 0)
        {
            if (x[r - 4][c - 3] == ' ' || cl < c) {
                c += 2;
                if (right < 168)
                {
                    right += 2;
                    left += 2;
                }
            }
        }
        if (flaggun == 1)
        {
            if (right < 168)
            {
                right += 6;
                left += 6;

            }
            c += 6;

        }

    }
    if (let == 'a')  //left
    {
        flag = 2;
        if (flaggun == 0)
        {
            if (x[r - 1][c - 15] == ' ')
            {
                c -= 2;
                if (left > 6)
                {
                    right -= 2;
                    left -= 2;
                }
            }
        }
        if (flaggun == 1)
        {
            c -= 6;
            if (left > 10)
            {
                right -= 6;
                left -= 6;
            }
        }

    }
    if (let == 'w') {

        if (flag == 1) {
            if (cl < c) {
                r -= 5;
            }
            else {
                r -= 5;
                c += 4;
            }
            if (right < 168)
            {
                right += 4;
                left += 4;

            }
            if (down > 5)
            {
                up -= 5;
                down -= 5;
            }
        }
        if (flag == 2) {
            r -= 5;
            c -= 4;
            if (right < 168)
            {
                right -= 4;
                left -= 4;

            }
            if (down > 5)
            {
                up -= 5;
                down -= 5;
            }
        }

    }

    if (let == 's' && x[r][c] == ' ')  //down
    {
        r += 2;
        if (up < 90)
        {

            up += 2;
            down += 2;
        }
    }


}

